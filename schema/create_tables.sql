CREATE TABLE BOOKS (
	BOOK_ID SERIAL PRIMARY KEY,
	TITLE VARCHAR(100) NOT NULL,
	AUTHOR VARCHAR(50),
	GENRE VARCHAR(50),
	PUBLISH_YEAR INT,
	PRICE NUMERIC(100, 2),
	STOCK INT
);

CREATE TABLE CUSTOMERS (
	CUSTOMER_ID SERIAL PRIMARY KEY,
	NAME CHAR(100) NOT NULL,
	EMAIL VARCHAR(100),
	PHONE NUMERIC(10),
	CITY VARCHAR(50),
	COUNTRY CHAR(50)
);

CREATE TABLE ORDERS (
	ORDER_ID SERIAL,
	CUSTOMER_ID SERIAL,
	BOOK_ID INT,
	ORDER_DATE DATE,
	QUANTITY INT,
	TOTAL_AMOUNT NUMERIC(100, 2)
);

